#!/bin/bash
a=70
b=100

#addition
total=$((a + b))

#subtraction
diff=$((a - b))

#multiplication
prod=$((a * b))

#Division
quot=$((a / b))

echo "total is :$total"   # 5 + 10
echo "The difference is : $diff"  # 5 - 10
echo "The multiplication is : $prod" # 5 x 10
echo "the quotient is: $quot"  # 5 / 10


